namespace FamilyMemories.Models
{
    /// <summary>
    /// 系統權限（前台／後台）
    /// </summary>
    public enum Permission
    {
        // ===== 前台權限 =====
        前台_瀏覽首頁 = 1,         // 首頁瀏覽
        前台_瀏覽回憶,           // 回憶列表與細節
        前台_新增回憶,           // 新增回憶（含上傳圖片）
        前台_編輯回憶,           // 編輯標題、描述、圖片
        前台_刪除回憶,           // 刪除自己的回憶
        前台_上傳照片,           // 單獨上傳照片
        前台_下載照片,           // 下載照片
        前台_留言,               // 回憶留言
        前台_編輯個人資料,       // 編輯個人資訊、頭像
        前台_檢視相簿,           // 相簿瀏覽
        // ===== 後台權限 =====
        後台_管理使用者 = 101,   // 使用者帳號啟用/停用、編輯
        後台_管理回憶,           // 所有回憶管理（含刪除、編輯）
        後台_管理照片,           // 所有照片管理
        後台_管理留言,           // 所有留言管理
        後台_管理角色,           // 角色建立、編輯、刪除
        後台_設定權限,           // 分配權限給角色/人員
        後台_檢視系統日誌,       // 系統操作紀錄
        後台_維護資料庫,         // 匯入/匯出/備份
        後台_網站設定            // 網站標題、Logo、主題等設定
    }
}
